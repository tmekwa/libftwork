#include <stdio.h>

int		ft_isalpha(char n)
{
	if ((n >= 65 && n <= 90) || (n >= 97 && n <= 122))
		return (1);
	else
		return (0);
}

int		main()
{
	char al = 66;

	printf("%d", ft_isalpha(al));
	return (0);
}
