#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int		main()
{
	char s1[] = "alive";
	char s2[] = "flower";

	printf("%s", ft_strcpy(s1, s2));
	return (0);
}
